# A partir de las variables, saca con solo print() y utilizando el operador de concatenación % 
# la siguiente frase: "La multiplicación de 10 * 34.50 da como resultado: 345.00."

numero_1 = 10
numero_2 = 34.50
resultado = numero_1 + numero_2

print("La multiplicación de %i * %.3f da como resultado: %.3f." %(numero_1, numero_2, resultado))